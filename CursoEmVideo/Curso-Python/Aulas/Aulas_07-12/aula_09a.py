frase = 'Curso em Vídeo Python'

print(frase)

print(frase[::2])

print(frase[9]) # mostra a letra dessa mini memoria

print(frase[9:14])

print(frase[9:21]) # vai de uma mini memoria a outra

print(frase[9:21:2]) # vai pulando de 2 em 2

print(frase[:5]) # vai do 0 ao 5 excluindo o 5

print(frase[15:]) # vai de um lugar até o seu final

print(frase[9::3]) # mostra de um número até o final pulando de 3 em 3
